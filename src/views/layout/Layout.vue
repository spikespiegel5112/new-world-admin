<template>
  <div class="common-main-container" :class="classObj">
    <div v-if="device==='mobile'&&sidebar.opened" class="drawer-bg" @click="handleClickOutside"></div>
    <sidebar class="sidebar-container"></sidebar>
    <div class="main-container">
      <navbar></navbar>
      <app-main></app-main>
    </div>
  </div>
</template>
<script>
  import {Navbar, Sidebar, AppMain} from './components'
  import ResizeMixin from './mixin/ResizeHandler'

  export default {
    name: 'layout',
    components: {
      Navbar,
      Sidebar,
      AppMain
    },
    mixins: [ResizeMixin],
    computed: {
      sidebar() {
        return this.$store.state.app.sidebar
      },
      device() {
        return this.$store.state.app.device
      },
      classObj() {
        return {
          hideSidebar: !this.sidebar.opened,
          withoutAnimation: this.sidebar.withoutAnimation,
          mobile: this.device === 'mobile'
        }
      },
      layoutReady() {
        return this.$store.state.app.layoutHeight > 0;
      }
    },
    created() {
      this.autoHeight();
    },
    mounted() {
    },
    methods: {
      handleClickOutside() {
        this.$store.dispatch('CloseSideBar', {withoutAnimation: false})
      },
      autoHeight() {
        this.$autoHeight({
          target: '.app-main',
          offset: -50
        });
        let layoutHeight = this.$autoHeight({
          target: '.app-main',
          reference: '.main-container',
          offset: -80,
          returnValue: true
        });
        console.log(this.$store)
        // layoutHeight = layoutHeight < 700 ? 700 : layoutHeight
        this.$nextTick(() => {
          this.$store.dispatch('updateLayoutHeight', layoutHeight);
        })
      },
    }
  }
</script>

